func twoSum1(numbers []int, target int) []int {
	mid := 0
	for mid = 0; mid < len(numbers); mid++ {
		if float64(numbers[mid]) == float64(target)/2 && float64(numbers[mid+1]) == float64(target)/2 {
			return []int{mid + 1, mid + 2}
		}
		if float64(numbers[mid]) > float64(target)/2 {
			mid -= 1
			break
		}
	}
	first, second := mid, mid+1
	for first > 0 {
		if numbers[first]+numbers[second] == target {
			break
		}
		if numbers[first]+numbers[second] < target {
			second += 1
		} else {
			first -= 1
		}
	}
	return []int{first + 1, second + 1}
}

